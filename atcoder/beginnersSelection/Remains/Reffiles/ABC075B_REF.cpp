// https://atcoder.jp/contests/abc075/submissions/1682902
int dx[8] = {1, 0, -1, 0, 1, -1, 1, -1};
int dy[8] = {0, 1, 0, -1, 1, -1, -1, 1};


int n, m;
vector<string> fi;

int main() {
    while (cin >> n >> m) {
        fi.clear();
        for (int i = 0; i < n; ++i) {
            string str;
            cin >> str;
            fi.push_back(str);
        }
        
        vector<vector<int> > res(n, vector<int>(m, 0));
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < m; ++j) {
                for (int k = 0; k < 8; ++k) {
                    int ni = i + dx[k], nj = j + dy[k];
                    if (ni < 0 || ni >= n || n < 0 || nj >= m) continue;
                    if (fi[ni][nj] == '#') {
                        ++res[i][j];
                    }
                }
            }
        }
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < m; ++j) {
                if (fi[i][j] == '.') cout << res[i][j];
                else cout << "#";
            }
            cout << endl;
        }
    }
}



