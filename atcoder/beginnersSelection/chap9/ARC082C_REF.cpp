int N;
int a[210000];

int main() {
  while (cin >> N) {
    for (int i = 0; i < N; ++i) cin >> a[i];
    int num[210000] = {0};
    for (int i = 0; i < N; ++i) {
      num[a[i]]++;
    }

    int res = 0;
    for (int i = 1; i <= 100000; ++i) {
      int tmp = num[i] + num[i-1] + num[i+1];
      chmax(res, tmp);
    }
    cout << res << endl;
  }
}












